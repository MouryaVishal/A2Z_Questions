string preToPost(string s) {
    stack<string> st;
    reverse(s.begin(),s.end());
    for(auto a:s){
        if(a=='/' || a=='+'||a=='*' ||a=='-'){
            string s1=st.top();
            st.pop();
            string s2=st.top();
            st.pop();
            st.push(s1+s2+a);
        }else{
            string ch(1,a);
            st.push(ch);
        }
    }

    return st.top();
}

