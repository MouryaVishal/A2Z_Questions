class BSTIterator {
    stack<TreeNode*> st;
    void traversal(TreeNode*node){
        for(;node!=NULL;st.push(node),node=node->left);
    }
public:
    BSTIterator(TreeNode* root) {
        traversal(root);
    }
    
    int next() {
        TreeNode* t=st.top();
        st.pop();
        traversal(t->right);
        return t->val;
    }
    
    bool hasNext() {
        return !st.empty();
    }
};

