class Solution {
public:
    bool isPowerOfTwo(int n) {
        if(n<=0)return false;
        int c=0;
        for(int i=0;i<32;i++){
            if((n&(1<<i))) c++;
            if(c>1)return false;
        }
        return true;
    }
};
