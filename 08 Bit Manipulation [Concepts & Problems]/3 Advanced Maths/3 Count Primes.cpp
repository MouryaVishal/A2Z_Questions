class Solution {
    vector<bool> sieve;
    void primeNumber(int n){

        for(int i=2;i<=sqrt(n);i++){
            if(sieve[i]){
                for(int j=i*i;j<=n;j+=i){
                    sieve[j]=false;
                }
            }
        }
    }
public:
    int countPrimes(int n) {
        sieve.resize(n+4,true);
        primeNumber(n);
        
        int cnt=0;
        for(int i=2;i<n;i++){
            if(sieve[i]){
                cnt++;
            }
        }

        return cnt;
    }
};
