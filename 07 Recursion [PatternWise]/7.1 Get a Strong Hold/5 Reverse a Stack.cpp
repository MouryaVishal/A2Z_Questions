class Solution{
    void inserAtBottom(stack<int>&a,int ele){
        if(a.size()==0){
            a.push(ele);
            return;
        }
        int top=a.top();
        a.pop();
        inserAtBottom(a,ele);
        a.push(top);
    }
public:
    void Reverse(stack<int> &st){
        if(st.size()==0)return;
        int a=st.top();
        st.pop();
        Reverse(st);
        inserAtBottom(st,a);
    }
};



