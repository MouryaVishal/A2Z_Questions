class Solution {
    void func(int &n,int s,int e,vector<string>&ans,string temp){
        if(s>n || e>n) return ;
        if(e==n){
            ans.push_back(temp);
            return;
        }
        if(s>e){
            func(n,s,e+1,ans,temp+')');
        }
        func(n,s+1,e,ans,temp+'(');
    }
public:
    vector<string> generateParenthesis(int n) {
       vector<string> ans;
       func(n,0,0,ans,"");
       return ans;
    }
};


